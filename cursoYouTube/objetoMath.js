// Es un objeto que nos permite hacer calculos matematicos 

// Algunos son los siguientes 

// Obtener el número PI 
console.log(Math.PI);

// Mostrar solo dos decimales 
console.log(Math.PI.toFixed(2));

// Quita los decimales 
console.log(Math.floor(Math.PI));

// pasar un número negativo a su valor absoluto o positivo 
console.log(Math.abs(-102));

// Calcular la potencia 
console.log(Math.pow(2,3));
console.log(2 ** 3); // Elevamos el número 2 a la potencia 3 

// Crea un número aleatorio entre 0 y 1 (no incluye el 1)
console.log(Math.random());

// Crea un número entre el 0 y el 10 con decimales 
console.log(Math.random() * 10 + 1);

// Me da un número entre el 0 y el 10 

console.log(Math.floor(Math.random() * 10 + 1));

// Si queremos aumentar el rango solamente modificamos el número por el cual se multiplica. 
